    private static byte[][] _cache;
    private static Vector2[] _centers;

    public static byte[][] Grid()
    {
        if (_cache != null) return _cache;
        _cache = new byte[Size][];
        for (int r = 0; r < Size; r++)
        {
            _cache[r] = new byte[Size];
            string row = Rows[r];
            for (int c = 0; c < Size; c++)
            {
                char ch = row[c];
                _cache[r][c] = ch == '.' ? (byte)0 : (byte)(ch - '0');
            }
        }
        return _cache;
    }

    /// <summary>Centre (x,y) en espace grille, pour chaque forme 1..ShapeCount.</summary>
    public static Vector2 CenterOf(int shapeId)
    {
        EnsureCenters();
        int i = Mathf.Clamp(shapeId, 1, ShapeCount) - 1;
        return _centers[i];
    }

    static void EnsureCenters()
    {
        if (_centers != null) return;
        var grid = Grid();
        _centers = new Vector2[ShapeCount];
        var sumX = new float[ShapeCount];
        var sumY = new float[ShapeCount];
        var count = new int[ShapeCount];

        for (int y = 0; y < Size; y++)
        {
            for (int x = 0; x < Size; x++)
            {
                byte id = grid[y][x];
                if (id == 0 || id > ShapeCount) continue;
                int i = id - 1;
                sumX[i] += x + 0.5f;
                sumY[i] += y + 0.5f;
                count[i]++;
            }
        }

        for (int i = 0; i < ShapeCount; i++)
        {
            if (count[i] > 0)
                _centers[i] = new Vector2(sumX[i] / count[i], sumY[i] / count[i]);
            else
                _centers[i] = new Vector2(Size * 0.5f, Size * 0.5f);
        }
    }
