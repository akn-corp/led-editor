    public static byte[][] Grid()
    {
        if (_cache != null) return _cache;
        _cache = new byte[Height][];
        for (int r = 0; r < Height; r++)
        {
            _cache[r] = new byte[Width];
            string row = Rows[r];
            for (int c = 0; c < Width; c++)
            {
                char ch = row[c];
                _cache[r][c] = ch == '.' ? (byte)0 : (byte)(ch - '0');
            }
        }
        return _cache;
    }
