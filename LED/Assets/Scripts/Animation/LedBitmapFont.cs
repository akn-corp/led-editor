    /// <summary>
    /// Bande de bits 3×5 pour défilement. Indexation : band[ligne][colonne].
    /// Largeur = 4 px / caractère (3 glyphe + 1 gap), comme EntityText.
    /// </summary>
    public static bool[][] BuildBand3x5(string message)
    {
        if (string.IsNullOrEmpty(message)) message = " ";
        message = message.ToUpperInvariant();

        var rows = new List<bool>[Glyph3x5Height];
        for (int r = 0; r < Glyph3x5Height; r++)
            rows[r] = new List<bool>();

        foreach (char c in message)
        {
            if (!Font3x5.TryGetValue(c, out string[] glyph))
                glyph = Font3x5[' '];

            for (int r = 0; r < Glyph3x5Height; r++)
            {
                foreach (char pixel in glyph[r])
                    rows[r].Add(pixel == '#');
                rows[r].Add(false);
            }
        }

        var band = new bool[Glyph3x5Height][];
        for (int r = 0; r < Glyph3x5Height; r++)
            band[r] = rows[r].ToArray();
        return band;
    }
